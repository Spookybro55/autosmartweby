// ============================================================================
// Section IDs — VERIFIED IN REPO
// buildPreviewBrief_() always starts with ['hero','services','contact'],
// conditionally adds 'location' (city||area), 'reviews' (rating>=3.5),
// 'faq' (painPoint non-empty).
// ============================================================================

export type SectionId = 'hero' | 'services' | 'contact' | 'reviews' | 'location' | 'faq';

// ============================================================================
// Website Status — VERIFIED IN REPO
// resolveWebsiteState_() in Helpers.gs returns UPPERCASE values.
// buildPreviewBrief_() stores .toLowerCase() in brief.
// ============================================================================

export type WebsiteStatusValue =
  | 'no_website'
  | 'weak_website'
  | 'has_website'
  | 'conflict'
  | 'unknown';

// ============================================================================
// Confidence Level — VERIFIED IN REPO
// Computed in buildPreviewBrief_(): score 0-6 →
//   >=5: 'high', >=3: 'medium', <3: 'low'
// ============================================================================

export type ConfidenceLevelValue = 'high' | 'medium' | 'low';

// ============================================================================
// Template Types — VERIFIED IN REPO
