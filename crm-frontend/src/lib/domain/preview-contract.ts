// ============================================================================
// Preview Brief — VERIFIED IN REPO
// Exact 18-field shape returned by buildPreviewBrief_() in PreviewPipeline.gs.
// All fields ALWAYS present (never undefined/null). Missing data → "" or [].
// ============================================================================

/**
 * CRM-side preview brief as generated by Apps Script.
 *
 * All fields are always present (never undefined/null). Empty data → "" or [].
 * This is the as-is shape stored in the `preview_brief_json` LEADS column.
 *
 * Validation tiers (PROPOSED RENDER RULES):
 *
 * HARD FAIL (renderer must reject if empty):
 *   business_name, city, headline, suggested_sections (min 3)
 *
 * DEGRADED RENDER (renderer continues with simplified output):
 *   subheadline → hero shows only H1
 *   key_benefits → services section without benefit cards
 *   cta → fallback text "Kontaktujte nás"
 *
 * ENRICHMENT (improves quality, not required):
 *   website_status, confidence_level, contact_phone, contact_email,
 *   contact_name, area, service_type, segment, pain_point, rating,
 *   reviews_count
 *
 * GAS generation guarantees (VERIFIED IN REPO):
 *   headline — always non-empty (buildNaturalHeadline_ fallback chain)
 *   subheadline — always non-empty (buildNaturalSubheadline_ fallback)
 *   cta — always non-empty (fallback: "Kontaktujte nás")
 *   suggested_sections — always min 3 (hero, services, contact)
 *   website_status — always non-empty (resolveWebsiteState_ always returns)
 *   confidence_level — always non-empty (computed score)
 *
 * suggested_sections renderer requirement: min 3 items.
 * GAS always generates min 3. Renderer matches this guarantee.
 */
export interface PreviewBrief {
  /** HARD FAIL if empty. GAS: may be "". */
  business_name: string;
  /** ENRICHMENT. GAS: may be "". */
  contact_name: string;
  /** HARD FAIL if empty. GAS: may be "". */
  city: string;
  /** ENRICHMENT. GAS: may be "". */
  area: string;
  /** ENRICHMENT. GAS: may be "". */
  service_type: string;
  /** ENRICHMENT. GAS: may be "". */
  segment: string;
  /** ENRICHMENT. GAS: may be "". */
  pain_point: string;
  /** HARD FAIL if empty. GAS: always non-empty (fallback chain). */
  headline: string;
  /** DEGRADED RENDER if empty — hero shows only H1. GAS: always non-empty. */
  subheadline: string;
  /** DEGRADED RENDER if empty — services section without cards. GAS: may be []. */
  key_benefits: string[];
  /** HARD FAIL if empty. GAS: always min 3. Renderer requires min 3. */
  suggested_sections: SectionId[];
  /** DEGRADED RENDER if empty — fallback: "Kontaktujte nás". GAS: always non-empty. */
  cta: string;
  /** ENRICHMENT. GAS: may be "". */
  contact_phone: string;
  /** ENRICHMENT. GAS: may be "". */
  contact_email: string;
  /** ENRICHMENT. GAS: always non-empty (lowercase from resolveWebsiteState_). */
  website_status: WebsiteStatusValue;
  /** ENRICHMENT. GAS: may be "". String type, numeric when present. */
  rating: string;
  /** ENRICHMENT. GAS: may be "". String type. */
  reviews_count: string;
  /** ENRICHMENT. GAS: always non-empty (computed). */
  confidence_level: ConfidenceLevelValue;
}

